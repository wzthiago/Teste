
public class ContagemRegressiva {

	public static void main(String[] args) {
		int count = 10;
		// Com essa correção, a contagem irá de 10 até 1, incluindo o número 1, e depois imprimirá "Feliz Ano Novo!".
		while (count > 0) { 
			System.out.println(count);
			count--;
		}
		System.out.println("Feliz Ano Novo!");
	
	}
}
